numeros = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]                        #lista de numeros predefinidos
print('Lista de números: ',numeros)                             #exibe lista de numeros predefinidos
print('\nMenor numero dessa lista: ',min(numeros))              #lista menor numero da lista com a função min()
print('\nMaior numero dessa lista: ',max(numeros))              #lista maior numero da lista com a função max()
print('\nSoma de todos os numeros dessa lista: ',sum(numeros))  #Exibe soma de todos os numeros da lista com a função sum()